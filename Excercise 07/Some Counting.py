#complete the following tasks using loops: from 0 to 50 in increments of 1, counts down from 50 to 0 in decrements of 1,
# counts up from 30 to 50 in increments of 1, counts down from 50 to 10 in decrements of 2, counts up from 100 to 200 in increments of 5.


# From 0 to 50 in increments of 1
# for loop
for i in range(51):
# Print Function.
    print(i)

# Print Function.
print("\n")

# Counts down from 50 to 0 in decrements of 1
# While statement
while i >= 0:
# Print Function.
    print(i)
#  decrement operator
    i = i-1

# Print Function.
print("\n")

# Counts up from 30 to 50 in increments of 1
# Variable h.
h = 30
# While Statement.
while h < 51:
# Print Function.
    print(h)
# Increment Operator
    h = h+1

# Print Function.
print("\n")

# Counts down from 50 to 10 in decrements of 2
# Variable k.
k = 50
# While statement.
while k >= 10:
# Print Function.
    print(k)
# Decrement operator
    k = k-2


# Print function.
print("\n")

